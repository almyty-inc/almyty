import React, { useState } from 'react'
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query'
import { useNavigate } from 'react-router-dom'
import { useForm } from 'react-hook-form'
import { zodResolver } from '@hookform/resolvers/zod'
import * as z from 'zod'
import { Code, Search, Play, Copy, Eye, Trash2, ExternalLink, Settings, Plus } from 'lucide-react'

import { Button } from '@/components/ui/button'
import { Input } from '@/components/ui/input'
import { Label } from '@/components/ui/label'
import { Textarea } from '@/components/ui/textarea'
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card'
import { Badge } from '@/components/ui/badge'
import { LoadingSpinner } from '@/components/ui/loading-spinner'
import { Switch } from '@/components/ui/switch'
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select'
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from '@/components/ui/dialog'
import {
  AlertDialog,
  AlertDialogAction,
  AlertDialogCancel,
  AlertDialogContent,
  AlertDialogDescription,
  AlertDialogFooter,
  AlertDialogHeader,
  AlertDialogTitle,
} from '@/components/ui/alert-dialog'
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
  DropdownMenuSeparator,
} from '@/components/ui/dropdown-menu'
import {
  DataTable,
  createSelectColumn,
  createActionsColumn,
  createSortableColumn,
} from '@/components/ui/data-table'
import { toolsApi } from '@/lib/api'
import { useOrganizationStore } from '@/store/organization'
import { useNotifications } from '@/store/app'
import { JsonSchemaBuilder } from '@/components/JsonSchemaBuilder'

// Form Schema for manual tool creation
const createToolSchema = z.object({
  name: z.string().min(1, 'Name is required'),
  description: z.string().optional(),
  type: z.enum(['function', 'action', 'query']),
})

type CreateToolForm = z.infer<typeof createToolSchema>

interface Tool {
  id: string
  name: string
  description?: string
  type: string
  status: string
  operation?: {
    method?: string
    path?: string
  }
  metadata?: {
    autoGenerated?: boolean
    sourceApi?: {
      id?: string
      name?: string
      type?: string
    }
    sourceOperation?: {
      name?: string
      method?: string
      endpoint?: string
    }
  }
  usage?: {
    total: number
    success: number
  }
  configuration?: {
    timeout?: number
    retries?: number
    cache?: {
      enabled: boolean
    }
  }
  createdAt?: string
  updatedAt?: string
}

export function ToolsPage() {
  const { currentOrganization } = useOrganizationStore()
  const queryClient = useQueryClient()
  const notifications = useNotifications()
  const navigate = useNavigate()

  const [searchQuery, setSearchQuery] = useState('')
  const [statusFilter, setStatusFilter] = useState('all')
  const [typeFilter, setTypeFilter] = useState('all')
  const [apiFilter, setApiFilter] = useState('all')
  const [selectedTool, setSelectedTool] = useState<Tool | null>(null)
  const [deletingTool, setDeletingTool] = useState<Tool | null>(null)
  const [toolForSettings, setToolForSettings] = useState<Tool | null>(null)
  const [toolForExecution, setToolForExecution] = useState<Tool | null>(null)
  const [isDetailsDialogOpen, setIsDetailsDialogOpen] = useState(false)
  const [isSettingsDialogOpen, setIsSettingsDialogOpen] = useState(false)
  const [isExecutionDialogOpen, setIsExecutionDialogOpen] = useState(false)
  const [isCreateDialogOpen, setIsCreateDialogOpen] = useState(false)
  const [executionParameters, setExecutionParameters] = useState<Record<string, any>>({})
  const [executionResult, setExecutionResult] = useState<any>(null)
  const [toolParameters, setToolParameters] = useState<any>({ type: 'object', properties: {} })

  const createForm = useForm<CreateToolForm>({
    resolver: zodResolver(createToolSchema),
    defaultValues: {
      name: '',
      description: '',
      type: 'function',
    },
  })

  const { data: toolsData, isLoading } = useQuery({
    queryKey: ['tools', currentOrganization?.id],
    queryFn: () => toolsApi.getAll(currentOrganization?.id),
    enabled: !!currentOrganization,
  })

  const deleteToolMutation = useMutation({
    mutationFn: (id: string) => toolsApi.delete(id),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['tools'] })
      notifications.success('Success', 'Tool deleted successfully')
      setDeletingTool(null)
    },
    onError: (error: any) => {
      notifications.error('Error', error.message || 'Failed to delete tool')
    },
  })

  const toggleStatusMutation = useMutation({
    mutationFn: ({ id, status }: { id: string; status: string }) =>
      toolsApi.update(id, { status }),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['tools'] })
      notifications.success('Success', 'Tool status updated')
    },
  })

  const createToolMutation = useMutation({
    mutationFn: (data: any) => toolsApi.create({ ...data, parameters: toolParameters }),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['tools'] })
      notifications.success('Success', 'Tool created successfully')
      setIsCreateDialogOpen(false)
      createForm.reset()
      setToolParameters({ type: 'object', properties: {} })
    },
    onError: (error: any) => {
      notifications.error('Error', error.response?.data?.message || 'Failed to create tool')
    },
  })

  const executeToolMutation = useMutation({
    mutationFn: ({ id, parameters }: { id: string; parameters: Record<string, any> }) =>
      toolsApi.execute(id, { parameters }),
    onSuccess: (response: any) => {
      setExecutionResult(response.data)
      if (response.data.success) {
        notifications.success('Success', 'Tool executed successfully')
      } else {
        notifications.error('Execution Failed', response.data.error || 'Tool execution failed')
      }
    },
    onError: (error: any) => {
      notifications.error('Error', error.message || 'Failed to execute tool')
      setExecutionResult({
        success: false,
        error: error.message || 'Failed to execute tool',
      })
    },
  })

  const tools = toolsData?.data?.data?.tools || toolsData?.data?.tools || []

  // Get unique API sources for filter
  const apiSources = Array.from(
    new Set(
      tools
        .map((t: Tool) => t.metadata?.sourceApi?.name)
        .filter(Boolean)
    )
  )

  const filteredTools = tools.filter((tool: Tool) => {
    const matchesSearch =
      tool.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
      (tool.description || '').toLowerCase().includes(searchQuery.toLowerCase()) ||
      (tool.metadata?.sourceApi?.name || '').toLowerCase().includes(searchQuery.toLowerCase())

    const matchesStatus = statusFilter === 'all' || tool.status === statusFilter
    const matchesType = typeFilter === 'all' || tool.type === typeFilter
    const matchesApi =
      apiFilter === 'all' || tool.metadata?.sourceApi?.name === apiFilter

    return matchesSearch && matchesStatus && matchesType && matchesApi
  })

  const handleCopyEndpoint = (tool: Tool) => {
    const endpoint = tool.metadata?.sourceOperation?.endpoint || tool.operation?.path || '/unknown'
    navigator.clipboard.writeText(endpoint)
    notifications.success('Copied', 'Endpoint copied to clipboard')
  }

  const handleViewDetails = (tool: Tool) => {
    navigate(`/tools/${tool.id}`)
  }

  const columns = [
    {
      accessorKey: 'name',
      header: 'Name',
      cell: ({ row }: any) => {
        const tool = row.original
        return (
          <div className="flex items-center space-x-3">
            <div className="w-10 h-10 bg-primary/10 rounded-lg flex items-center justify-center">
              <Code className="h-5 w-5 text-primary" />
            </div>
            <div>
              <div className="font-medium">{tool.name}</div>
              <div className="text-sm text-muted-foreground">
                {tool.metadata?.sourceApi?.name || 'Unknown API'}
              </div>
            </div>
          </div>
        )
      },
    },
    {
      accessorKey: 'type',
      header: 'Type',
      cell: ({ row }: any) => <Badge variant="outline">{row.original.type}</Badge>,
    },
    {
      accessorKey: 'method',
      header: 'Method',
      cell: ({ row }: any) => {
        const method =
          row.original.operation?.method ||
          row.original.metadata?.sourceOperation?.method ||
          'GET'
        return <Badge className="font-mono">{method}</Badge>
      },
    },
    {
      accessorKey: 'status',
      header: 'Status',
      cell: ({ row }: any) => {
        const status = row.original.status
        const variant = status === 'active' ? 'success' : 'secondary'
        return (
          <div className="flex items-center gap-2">
            <Badge variant={variant}>{status}</Badge>
            <Switch
              checked={status === 'active'}
              onCheckedChange={(checked) => {
                toggleStatusMutation.mutate({
                  id: row.original.id,
                  status: checked ? 'active' : 'inactive',
                })
              }}
              aria-label="Toggle tool status"
            />
          </div>
        )
      },
    },
    createActionsColumn<Tool>(
      (tool) => handleViewDetails(tool),
      (tool) => setDeletingTool(tool),
      [
        {
          label: 'View Details',
          onClick: (tool) => handleViewDetails(tool),
        },
        {
          label: 'Test Tool',
          onClick: (tool) => {
            setToolForExecution(tool)
            setExecutionParameters({})
            setExecutionResult(null)
            setIsExecutionDialogOpen(true)
          },
        },
        {
          label: 'Settings',
          onClick: (tool) => {
            setToolForSettings(tool)
            setIsSettingsDialogOpen(true)
          },
        },
        {
          label: 'Copy Endpoint',
          onClick: (tool) => handleCopyEndpoint(tool),
        },
      ]
    ),
  ]

  if (!currentOrganization) {
    return (
      <div className="flex items-center justify-center h-96">
        <div className="text-center">
          <p className="text-muted-foreground">No organization context</p>
        </div>
      </div>
    )
  }

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-96">
        <LoadingSpinner size="lg" />
      </div>
    )
  }

  return (
    <div className="space-y-8">
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-3xl font-bold tracking-tight">Tools</h1>
          <p className="text-muted-foreground">
            AI tools generated from your APIs
          </p>
        </div>
        <Button onClick={() => setIsCreateDialogOpen(true)} disabled={!currentOrganization}>
          <Plus className="mr-2 h-4 w-4" />
          Create Tool
        </Button>
      </div>

      {/* Stats Cards */}
      <div className="grid gap-4 md:grid-cols-3">
        <Card>
          <CardHeader className="pb-3">
            <CardTitle className="text-sm">Total Tools</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{tools.length}</div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-3">
            <CardTitle className="text-sm">Active Tools</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold text-green-600">
              {tools.filter((t: Tool) => t.status === 'active').length}
            </div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-3">
            <CardTitle className="text-sm">Auto-Generated</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold text-blue-600">
              {tools.filter((t: Tool) => t.metadata?.autoGenerated).length}
            </div>
          </CardContent>
        </Card>
      </div>

      {/* Tools Table */}
      {tools.length === 0 ? (
        <Card>
          <CardContent className="flex flex-col items-center justify-center py-16">
            <div className="w-16 h-16 bg-primary/10 rounded-full flex items-center justify-center mb-4">
              <Code className="h-8 w-8 text-primary" />
            </div>
            <h3 className="text-xl font-semibold mb-2">No tools</h3>
            <p className="text-muted-foreground mb-6 text-center max-w-md">
              Generate tools from API schemas automatically. Create your first tool by importing an API.
            </p>
            <Button size="lg" asChild>
              <a href="/apis">
                <Plus className="mr-2 h-4 w-4" />
                Go to APIs
              </a>
            </Button>
          </CardContent>
        </Card>
      ) : (
        <Card>
          <CardContent className="pt-6 space-y-4">
            {/* Filters */}
            <div className="flex items-center gap-4">
              <div className="flex-1">
                <div className="relative">
                  <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-muted-foreground" />
                  <Input
                    placeholder="Search tools..."
                    className="pl-10"
                    value={searchQuery}
                    onChange={(e) => setSearchQuery(e.target.value)}
                  />
                </div>
              </div>
              <Select value={statusFilter} onValueChange={setStatusFilter}>
                <SelectTrigger className="w-32">
                  <SelectValue placeholder="Status" />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">All Status</SelectItem>
                  <SelectItem value="active">Active</SelectItem>
                  <SelectItem value="inactive">Inactive</SelectItem>
                </SelectContent>
              </Select>
              <Select value={typeFilter} onValueChange={setTypeFilter}>
                <SelectTrigger className="w-32" aria-label="Filter by Type">
                  <SelectValue placeholder="Type" />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">All Types</SelectItem>
                  <SelectItem value="REST_API">REST</SelectItem>
                  <SelectItem value="GRAPHQL">GraphQL</SelectItem>
                  <SelectItem value="SOAP">SOAP</SelectItem>
                </SelectContent>
              </Select>
              {apiSources.length > 0 && (
                <Select value={apiFilter} onValueChange={setApiFilter}>
                  <SelectTrigger className="w-48" aria-label="Filter by API">
                    <SelectValue placeholder="Filter by API" />
                  </SelectTrigger>
                  <SelectContent>
                    <SelectItem value="all">All APIs</SelectItem>
                    {apiSources.map((apiName: string) => (
                      <SelectItem key={apiName} value={apiName}>
                        {apiName}
                      </SelectItem>
                    ))}
                  </SelectContent>
                </Select>
              )}
            </div>

            <DataTable
              columns={columns}
              data={filteredTools}
              loading={isLoading}
              onRowClick={(tool) => handleViewDetails(tool)}
            />
          </CardContent>
        </Card>
      )}

      {/* Tool Details Dialog */}
      <Dialog open={isDetailsDialogOpen} onOpenChange={setIsDetailsDialogOpen}>
        <DialogContent className="max-w-3xl">
          <DialogHeader>
            <DialogTitle className="flex items-center gap-3">
              <Code className="h-5 w-5" />
              {selectedTool?.name}
            </DialogTitle>
            <DialogDescription>
              {selectedTool?.description || 'AI tool generated from API operation'}
            </DialogDescription>
          </DialogHeader>

          {selectedTool && (
            <div className="space-y-4">
              <div className="grid grid-cols-2 gap-4">
                <div>
                  <h4 className="text-sm font-medium mb-2">Source API</h4>
                  <div className="text-sm space-y-1">
                    <div>
                      <span className="text-muted-foreground">API: </span>
                      {selectedTool.metadata?.sourceApi?.name || 'Unknown'}
                    </div>
                    <div>
                      <span className="text-muted-foreground">Type: </span>
                      {selectedTool.metadata?.sourceApi?.type || 'Unknown'}
                    </div>
                    <div>
                      <span className="text-muted-foreground">Operation: </span>
                      {selectedTool.metadata?.sourceOperation?.name || 'Unknown'}
                    </div>
                    <div>
                      <span className="text-muted-foreground">Method: </span>
                      <Badge className="font-mono">
                        {selectedTool.metadata?.sourceOperation?.method ||
                          selectedTool.operation?.method ||
                          'GET'}
                      </Badge>
                    </div>
                    <div>
                      <span className="text-muted-foreground">Endpoint: </span>
                      <code className="text-xs bg-muted px-1 py-0.5 rounded">
                        {selectedTool.metadata?.sourceOperation?.endpoint ||
                          selectedTool.operation?.path ||
                          'Unknown'}
                      </code>
                    </div>
                  </div>
                </div>

                <div>
                  <h4 className="text-sm font-medium mb-2">Configuration</h4>
                  <div className="text-sm space-y-1">
                    <div>
                      <span className="text-muted-foreground">Status: </span>
                      <Badge
                        variant={
                          selectedTool.status === 'active' ? 'success' : 'secondary'
                        }
                      >
                        {selectedTool.status}
                      </Badge>
                    </div>
                    <div>
                      <span className="text-muted-foreground">Timeout: </span>
                      {selectedTool.configuration?.timeout || 30000}ms
                    </div>
                    <div>
                      <span className="text-muted-foreground">Retries: </span>
                      {selectedTool.configuration?.retries || 3}
                    </div>
                    <div>
                      <span className="text-muted-foreground">Cache: </span>
                      {selectedTool.configuration?.cache?.enabled
                        ? 'Enabled'
                        : 'Disabled'}
                    </div>
                    <div>
                      <span className="text-muted-foreground">Auto-generated: </span>
                      {selectedTool.metadata?.autoGenerated ? 'Yes' : 'No'}
                    </div>
                  </div>
                </div>
              </div>

              <div className="border-t pt-4">
                <h4 className="text-sm font-medium mb-2">Assigned Gateways</h4>
                <div className="flex gap-2 flex-wrap">
                  {selectedTool?.gatewayAssociations?.length > 0 ? (
                    selectedTool.gatewayAssociations.map((assoc: any) => (
                      <Badge key={assoc.id} variant="secondary">
                        {assoc.gateway?.name || 'Unknown'} ({assoc.gateway?.type?.toUpperCase() || 'N/A'})
                      </Badge>
                    ))
                  ) : (
                    <p className="text-sm text-muted-foreground">
                      Not assigned to any gateway. Go to <a href="/gateways" className="underline">Gateways page</a> to assign this tool.
                    </p>
                  )}
                </div>
              </div>

              <div className="flex gap-2 pt-4 border-t">
                <Button
                  size="sm"
                  variant="outline"
                  onClick={() => handleCopyEndpoint(selectedTool)}
                >
                  <Copy className="h-3 w-3 mr-1" />
                  Copy Endpoint
                </Button>
                <Button
                  size="sm"
                  variant="outline"
                  onClick={() => {
                    // Execute tool with empty parameters for quick test
                    if (selectedTool) {
                      executeToolMutation.mutate({
                        toolId: selectedTool.id,
                        parameters: {}
                      })
                    }
                  }}
                  disabled={executeToolMutation.isPending}
                >
                  <Play className="h-3 w-3 mr-1" />
                  {executeToolMutation.isPending ? 'Testing...' : 'Test Tool'}
                </Button>
                <Button
                  size="sm"
                  variant="outline"
                  onClick={() => {
                    // Close details dialog and open execution dialog to show parameters
                    setIsDetailsDialogOpen(false)
                    setToolForExecution(selectedTool)
                    setIsExecutionDialogOpen(true)
                  }}
                >
                  <ExternalLink className="h-3 w-3 mr-1" />
                  View Parameters
                </Button>
              </div>
            </div>
          )}
        </DialogContent>
      </Dialog>

      {/* Tool Settings Dialog */}
      <Dialog open={isSettingsDialogOpen} onOpenChange={setIsSettingsDialogOpen}>
        <DialogContent className="max-w-2xl">
          <DialogHeader>
            <DialogTitle className="flex items-center gap-3">
              <Settings className="h-5 w-5" />
              Tool Settings
            </DialogTitle>
            <DialogDescription>
              Configure {toolForSettings?.name} execution settings
            </DialogDescription>
          </DialogHeader>

          {toolForSettings && (
            <div className="space-y-6">
              <div className="space-y-4">
                <div>
                  <h4 className="text-sm font-medium mb-3">Execution Configuration</h4>
                  <div className="space-y-4">
                    <div>
                      <label className="text-sm font-medium">Timeout (ms)</label>
                      <Input
                        type="number"
                        defaultValue={toolForSettings.configuration?.timeout || 30000}
                        className="mt-1"
                        placeholder="30000"
                      />
                      <p className="text-xs text-muted-foreground mt-1">
                        Maximum time in milliseconds to wait for tool execution
                      </p>
                    </div>

                    <div>
                      <label className="text-sm font-medium">Retries</label>
                      <Input
                        type="number"
                        defaultValue={toolForSettings.configuration?.retries || 3}
                        className="mt-1"
                        min="0"
                        max="10"
                        placeholder="3"
                      />
                      <p className="text-xs text-muted-foreground mt-1">
                        Number of retry attempts on failure
                      </p>
                    </div>

                    <div>
                      <label className="text-sm font-medium">Rate Limit (requests/min)</label>
                      <Input
                        type="number"
                        defaultValue={60}
                        className="mt-1"
                        min="1"
                        placeholder="60"
                      />
                      <p className="text-xs text-muted-foreground mt-1">
                        Maximum number of requests per minute
                      </p>
                    </div>
                  </div>
                </div>

                <div className="border-t pt-4">
                  <h4 className="text-sm font-medium mb-3">Caching</h4>
                  <div className="flex items-center justify-between">
                    <div>
                      <label className="text-sm font-medium">Enable Cache</label>
                      <p className="text-xs text-muted-foreground">
                        Cache successful responses for faster repeated requests
                      </p>
                    </div>
                    <Switch
                      defaultChecked={toolForSettings.configuration?.cache?.enabled}
                    />
                  </div>
                  <div className="mt-3">
                    <label className="text-sm font-medium">Cache TTL (seconds)</label>
                    <Input
                      type="number"
                      defaultValue={300}
                      className="mt-1"
                      placeholder="300"
                    />
                  </div>
                </div>

                <div className="border-t pt-4">
                  <h4 className="text-sm font-medium mb-3">Authentication</h4>
                  <Select defaultValue="inherit">
                    <SelectTrigger>
                      <SelectValue placeholder="Select authentication" />
                    </SelectTrigger>
                    <SelectContent>
                      <SelectItem value="inherit">Inherit from API</SelectItem>
                      <SelectItem value="none">None</SelectItem>
                      <SelectItem value="bearer">Bearer Token</SelectItem>
                      <SelectItem value="api-key">API Key</SelectItem>
                    </SelectContent>
                  </Select>
                </div>
              </div>

              <div className="flex justify-end gap-2 pt-4 border-t">
                <Button
                  variant="outline"
                  onClick={() => setIsSettingsDialogOpen(false)}
                >
                  Cancel
                </Button>
                <Button onClick={() => {
                  notifications.success('Settings Updated', 'Tool configuration saved successfully')
                  setIsSettingsDialogOpen(false)
                }}>
                  Save Settings
                </Button>
              </div>
            </div>
          )}
        </DialogContent>
      </Dialog>

      {/* Tool Execution Dialog */}
      <Dialog open={isExecutionDialogOpen} onOpenChange={setIsExecutionDialogOpen}>
        <DialogContent className="max-w-3xl max-h-[80vh] overflow-y-auto">
          <DialogHeader>
            <DialogTitle className="flex items-center gap-3">
              <Play className="h-5 w-5" />
              Test Tool: {toolForExecution?.name}
            </DialogTitle>
            <DialogDescription>
              Execute the tool with parameters and view results
            </DialogDescription>
          </DialogHeader>

          {toolForExecution && (
            <div className="space-y-6">
              {/* Parameters Section */}
              <div id="tool-parameters-section" className="space-y-4">
                <h4 className="text-sm font-medium">Parameters</h4>

                {/* Simple parameter inputs - expand based on tool.parameters schema */}
                <div className="space-y-3">
                  <div>
                    <label className="text-sm font-medium">Path Parameters</label>
                    <Input
                      placeholder='{"petId": "1"}'
                      value={JSON.stringify(executionParameters.path || {})}
                      onChange={(e) => {
                        try {
                          const parsed = JSON.parse(e.target.value || '{}')
                          setExecutionParameters({ ...executionParameters, path: parsed })
                        } catch {}
                      }}
                      className="mt-1 font-mono text-xs"
                    />
                  </div>

                  <div>
                    <label className="text-sm font-medium">Query Parameters</label>
                    <Input
                      placeholder='{"status": "available"}'
                      value={JSON.stringify(executionParameters.query || {})}
                      onChange={(e) => {
                        try {
                          const parsed = JSON.parse(e.target.value || '{}')
                          setExecutionParameters({ ...executionParameters, query: parsed })
                        } catch {}
                      }}
                      className="mt-1 font-mono text-xs"
                    />
                  </div>

                  <div>
                    <label className="text-sm font-medium">Body</label>
                    <textarea
                      placeholder='{"name": "Rex", "status": "available"}'
                      value={JSON.stringify(executionParameters.body || {}, null, 2)}
                      onChange={(e) => {
                        try {
                          const parsed = JSON.parse(e.target.value || '{}')
                          setExecutionParameters({ ...executionParameters, body: parsed })
                        } catch {}
                      }}
                      className="w-full min-h-[100px] p-2 border rounded font-mono text-xs"
                    />
                  </div>
                </div>
              </div>

              {/* Execute Button */}
              <div className="flex justify-between items-center pt-4 border-t">
                <Button
                  onClick={() => {
                    if (toolForExecution) {
                      executeToolMutation.mutate({
                        id: toolForExecution.id,
                        parameters: executionParameters,
                      })
                    }
                  }}
                  disabled={executeToolMutation.isPending}
                >
                  {executeToolMutation.isPending ? (
                    <>
                      <LoadingSpinner size="sm" className="mr-2" />
                      Executing...
                    </>
                  ) : (
                    <>
                      <Play className="h-4 w-4 mr-2" />
                      Execute
                    </>
                  )}
                </Button>

                {executionResult && (
                  <div className="text-xs text-muted-foreground">
                    {executionResult.metadata?.executionTime && (
                      <span className="mr-3">
                        {executionResult.metadata.executionTime}ms
                      </span>
                    )}
                    {executionResult.cached && (
                      <Badge variant="secondary" className="text-xs">Cached</Badge>
                    )}
                  </div>
                )}
              </div>

              {/* Results Section */}
              {executionResult && (
                <div className="space-y-3 border-t pt-4">
                  <div className="flex items-center justify-between">
                    <h4 className="text-sm font-medium">Result</h4>
                    <Badge variant={executionResult.success ? 'success' : 'destructive'}>
                      {executionResult.success ? 'Success' : 'Error'}
                    </Badge>
                  </div>

                  {executionResult.success ? (
                    <div className="space-y-2">
                      <div className="bg-green-50 border border-green-200 rounded p-3">
                        <pre className="text-xs overflow-x-auto">
                          {JSON.stringify(executionResult.data, null, 2)}
                        </pre>
                      </div>
                      {executionResult.metadata?.executionTime && (
                        <p className="text-xs text-muted-foreground">
                          Execution time: {executionResult.metadata.executionTime}ms
                          {executionResult.metadata.httpStatus && ` | HTTP ${executionResult.metadata.httpStatus}`}
                        </p>
                      )}
                    </div>
                  ) : (
                    <div className="bg-red-50 border border-red-200 rounded p-3">
                      <p className="text-sm text-red-900 font-medium">Error:</p>
                      <p className="text-xs text-red-700 mt-1">{executionResult.error}</p>
                    </div>
                  )}
                </div>
              )}
            </div>
          )}
        </DialogContent>
      </Dialog>

      {/* Delete Confirmation Dialog */}
      <AlertDialog
        open={!!deletingTool}
        onOpenChange={(open) => !open && setDeletingTool(null)}
      >
        <AlertDialogContent>
          <AlertDialogHeader>
            <AlertDialogTitle>Are you sure?</AlertDialogTitle>
            <AlertDialogDescription>
              This will permanently delete the tool "{deletingTool?.name}". This
              action cannot be undone.
            </AlertDialogDescription>
          </AlertDialogHeader>
          <AlertDialogFooter>
            <AlertDialogCancel>Cancel</AlertDialogCancel>
            <AlertDialogAction
              onClick={() => {
                if (deletingTool) {
                  deleteToolMutation.mutate(deletingTool.id)
                }
              }}
              className="bg-destructive text-destructive-foreground hover:bg-destructive/90"
            >
              Delete
            </AlertDialogAction>
          </AlertDialogFooter>
        </AlertDialogContent>
      </AlertDialog>

      {/* Create Tool Dialog */}
      <Dialog open={isCreateDialogOpen} onOpenChange={setIsCreateDialogOpen}>
        <DialogContent className="max-w-2xl">
          <DialogHeader>
            <DialogTitle>Create Manual Tool</DialogTitle>
            <DialogDescription>
              Create a custom tool manually. For API-based tools, import an API schema instead.
            </DialogDescription>
          </DialogHeader>
          <form onSubmit={createForm.handleSubmit((data) => createToolMutation.mutate(data))} className="space-y-4">
            <div>
              <Label htmlFor="tool-name">Tool Name</Label>
              <Input
                id="tool-name"
                placeholder="my_custom_tool"
                {...createForm.register('name')}
              />
              {createForm.formState.errors.name && (
                <p className="text-sm text-red-500 mt-1">{createForm.formState.errors.name.message}</p>
              )}
            </div>

            <div>
              <Label htmlFor="tool-description">Description</Label>
              <Textarea
                id="tool-description"
                placeholder="What does this tool do?"
                {...createForm.register('description')}
              />
            </div>

            <div>
              <Label htmlFor="tool-type">Tool Type</Label>
              <Select
                onValueChange={(value) => createForm.setValue('type', value as any)}
                value={createForm.watch('type')}
              >
                <SelectTrigger id="tool-type">
                  <SelectValue />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="function">Function</SelectItem>
                  <SelectItem value="action">Action</SelectItem>
                  <SelectItem value="query">Query</SelectItem>
                </SelectContent>
              </Select>
            </div>

            <div>
              <JsonSchemaBuilder
                value={toolParameters}
                onChange={setToolParameters}
              />
            </div>

            <div className="flex justify-end space-x-2">
              <Button
                type="button"
                variant="outline"
                onClick={() => setIsCreateDialogOpen(false)}
              >
                Cancel
              </Button>
              <Button type="submit" disabled={createToolMutation.isPending}>
                {createToolMutation.isPending ? 'Creating...' : 'Create Tool'}
              </Button>
            </div>
          </form>
        </DialogContent>
      </Dialog>
    </div>
  )
}
